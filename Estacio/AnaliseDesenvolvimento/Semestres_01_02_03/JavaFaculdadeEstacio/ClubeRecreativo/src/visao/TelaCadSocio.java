    @Override
    void trataInclui() {
        if (txtNome.getText() == null || txtNome.getText().isEmpty() || txtMatricula.getText() == null || txtMatricula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else {
            int matricula = Integer.parseInt(txtMatricula.getText());
            String nome = txtNome.getText();
            Socio soc = new Socio(matricula, nome);
            JOptionPane.showMessageDialog(null, "Socio criado\n" + soc.toString());
        }
    }

